package javaEssential.hw2.task2;

public class Car {
    private int year;
    private String colour;

    public Car(int year) {
        this.year = year;
    }

    public Car(String colour) {
        this.colour = colour;
    }

    public Car(int year, String colour) {
        this.year = year;
        this.colour = colour;
    }
}
